import random

def играть_в_камень_ножницы_бумагу():
    варианты = ["камень", "ножницы", "бумага"]
    
    while True:
        компьютер = random.choice(варианты)
        игрок = input("Выберите камень, ножницы или бумагу (для выхода введите 'выход'): ").lower()
        
        if игрок == "выход":
            break
        
        if игрок not in варианты:
            print("Некорректный ввод. Пожалуйста, выберите камень, ножницы или бумагу.")
            continue
        
        print(f"Вы выбрали: {игрок}")
        print(f"Компьютер выбрал: {компьютер}")
        
        if игрок == компьютер:
            print("Ничья!")
        elif (игрок == "камень" and компьютер == "ножницы") or \
             (игрок == "ножницы" and компьютер == "бумага") or \
             (игрок == "бумага" and компьютер == "камень"):
            print("Вы выиграли!")
        else:
            print("Компьютер выиграл!")

if __name__ == "__main__":
    играть_в_камень_ножницы_бумагу()